# Years of Potential Life Lost Rate per 10,000 Population (U75) Pooled Data of Deaths

# The years of life lost (YLL) is a summary measure of premature mortality. YLL estimates the years of potential life lost due to premature deaths. YLL takes into account the age at which deaths occur, giving greater weight to deaths at a younger age and lower weight to deaths at older age.

# YLL can be used to calculate the YLL due to a specific cause of death as a proportion of the total YLL lost in the population due to premature mortality. Such indicator can be used in public health planning to compare the relative importance of different causes of premature deaths within a given population, to set priorities for prevention, and to compare the premature mortality experience between populations.

# Calculating the number of years of life lost
# 
# The number of YLL is calculated by summing the number of deaths at each age between 1-74 years, multiplied by the number of years of life remaining up to the age of 75 years.1 (Note that the upper limit approximates life expectancy in a given population and any upper age limit could potentially be used. Deaths at age <1 year are excluded as they are often related to causes originating in perinatal period such as congenital anomalies or prematurity.)
# 
# For example, if there were ten deaths at the age of 1, the YLL contribution of this cause of death and age group would be:
#   
#   Number of deaths at the age of 1 year
# x The number of years lost had each individual lived to the age of 75
# = 10 x 74 years
# = 740 years
# 
# This calculation is done for deaths due to this cause in each age group, and the results are then summed. Mathematically, these calculations can be summarised as follows:

# YLL = \sum_{i=1}^{74}a_id_i

# where a_i = 75 - i is the number of years lost and d_i is the number of deaths at age i (i.e. between i and i+1) in the population
  
# The crude rate of life years lost is the calculated YLL divided by the total U75 population, multiplied by 100,000


